%%==============================================================================
%% The 'els_db_table' behaviour
%%==============================================================================

-module(els_db_table).

%%==============================================================================
%% Callbacks
%%==============================================================================

-callback name() -> atom().
-callback opts() -> proplists:proplist().

%%==============================================================================
%% Exports
%%==============================================================================

-export([ create/1 ]).

%%==============================================================================
%% API
%%==============================================================================

-spec create(module()) -> ok | {error, any()}.
create(Table) ->
  case mnesia:create_table(Table:name(), Table:opts()) of
    {atomic, ok} ->
      ok;
    Error ->
      {error, Error}
  end.
